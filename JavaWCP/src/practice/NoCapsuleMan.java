package practice;

public class NoCapsuleMan {
	private String name;
	private int age;
	
	public NoCapsuleMan(String name, int age) {
		this.name = name;
		this.age = age;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public int setAge(int age) {
		return this.age;
	}
}
