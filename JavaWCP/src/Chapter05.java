
public class Chapter05 {
	public static void main(String[] args) {
		long a = 112233L;
		double b = 1122.33;
		boolean c = true;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		Float d = 1122.33f;
		Integer e = 3;
		Character f = 'A';
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		
		int[] g = { 1, 2, 3, 4, 5 };
		System.out.println(g[3]);
	}
}
